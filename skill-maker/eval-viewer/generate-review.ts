#!/usr/bin/env bun

/**
 * generate-review.ts — Generate a self-contained HTML review page for eval results
 *
 * Part of the Agent Skills evaluation system. Reads eval outputs, grading, and
 * benchmark data from an iteration directory and produces a single HTML file
 * that can be opened in any browser for offline review.
 *
 * Usage:
 *   bun run eval-viewer/generate-review.ts <iteration-dir> --skill-name <name>
 *   bun run eval-viewer/generate-review.ts workspace/iteration-1 --skill-name my-skill
 */

import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { basename, join, resolve } from "node:path";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

interface OutputFile {
  name: string;
  content: string;
}

interface EvalData {
  eval_name: string;
  prompt: string;
  with_skill: {
    outputs: OutputFile[];
    grading: object | null;
    timing: object | null;
  };
  without_skill: {
    outputs: OutputFile[];
    grading: object | null;
    timing: object | null;
  } | null;
}

interface CliArgs {
  iterationDir: string;
  skillName: string;
  output: string | null;
  previousWorkspace: string | null;
  benchmarkPath: string | null;
}

