// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function die(message: string, hint?: string): never {
  console.error(`\n  ✗ Error: ${message}`);
  if (hint) console.error(`    → ${hint}`);
  console.error();
  process.exit(1);
}

/** Safely read and parse a JSON file, returning null on failure. */
function readJsonSafe(path: string): object | null {
  try {
    if (!existsSync(path)) return null;
    const raw = readFileSync(path, "utf-8");
    return JSON.parse(raw);
  } catch {
    return null;
  }
}

/** Read all files from a directory (non-recursive), returning name+content pairs. */
function readOutputFiles(dir: string): OutputFile[] {
  if (!existsSync(dir)) return [];

  const entries = readdirSync(dir);
  const files: OutputFile[] = [];

  for (const entry of entries) {
    const fullPath = join(dir, entry);
    try {
      const stat = statSync(fullPath);
      if (!stat.isFile()) continue;

      let content = "";
      try {
        content = readFileSync(fullPath, "utf-8");
      } catch {
        content = "(binary or unreadable file)";
      }

      files.push({ name: entry, content });
    } catch {
      // skip entries we can't stat
    }
  }

  return files;
}

// ---------------------------------------------------------------------------
// Eval discovery
// ---------------------------------------------------------------------------

/** Discover and load eval data from an iteration directory. */
function discoverEvals(iterationDir: string): EvalData[] {
  if (!existsSync(iterationDir)) {
    die(
      `Directory not found: ${iterationDir}`,
      "Make sure the iteration directory exists."
    );
  }

  const entries = readdirSync(iterationDir)
    .filter((e) => {
      const fullPath = join(iterationDir, e);
      return e.startsWith("eval-") && existsSync(fullPath) && statSync(fullPath).isDirectory();
    })
    .sort();

  if (entries.length === 0) {
    die(
      `No eval directories found in ${iterationDir}`,
      'Expected directories starting with "eval-" (e.g., eval-basic-usage, eval-0).'
    );
  }

  const evals: EvalData[] = [];

  for (const evalDirName of entries) {
    const evalDir = join(iterationDir, evalDirName);

    // Read eval_metadata.json
    const metadataPath = join(evalDir, "eval_metadata.json");
    const metadata = readJsonSafe(metadataPath) as { prompt?: string; eval_name?: string } | null;

    const evalName = metadata?.eval_name || evalDirName;
    const prompt = metadata?.prompt || "(no prompt found)";

    // With-skill data
    const withSkillDir = join(evalDir, "with_skill");
    const withSkillOutputs = readOutputFiles(join(withSkillDir, "outputs"));
    const withSkillGrading = readJsonSafe(join(withSkillDir, "grading.json"));
    const withSkillTiming = readJsonSafe(join(withSkillDir, "timing.json"));

    // Without-skill data (optional)
    const withoutSkillDir = join(evalDir, "without_skill");
    let withoutSkill: EvalData["without_skill"] = null;

    if (existsSync(withoutSkillDir)) {
      const withoutSkillOutputs = readOutputFiles(join(withoutSkillDir, "outputs"));
      const withoutSkillGrading = readJsonSafe(join(withoutSkillDir, "grading.json"));
      const withoutSkillTiming = readJsonSafe(join(withoutSkillDir, "timing.json"));

      if (withoutSkillOutputs.length > 0 || withoutSkillGrading || withoutSkillTiming) {
        withoutSkill = {
          outputs: withoutSkillOutputs,
          grading: withoutSkillGrading,
          timing: withoutSkillTiming,
        };
      }
    }

    evals.push({
      eval_name: evalName,
      prompt,
      with_skill: {
        outputs: withSkillOutputs,
        grading: withSkillGrading,
        timing: withSkillTiming,
      },
      without_skill: withoutSkill,
    });
  }

  return evals;
}

// ---------------------------------------------------------------------------
// CLI argument parsing
// ---------------------------------------------------------------------------

function parseArgs(): CliArgs {
  const args = process.argv.slice(2);

  // --help
  if (args.includes("--help") || args.includes("-h")) {
    console.log(HELP);
    process.exit(0);
  }

  // Find positional argument (iteration-dir)
  let iterationDir: string | null = null;
  let skillName: string | null = null;
  let output: string | null = null;
  let previousWorkspace: string | null = null;
  let benchmarkPath: string | null = null;

  let i = 0;
  while (i < args.length) {
    const arg = args[i];

    if (arg === "--skill-name") {
      i++;
      if (i >= args.length) die("--skill-name requires a value");
      skillName = args[i];
    } else if (arg === "--output") {
      i++;
      if (i >= args.length) die("--output requires a value");
      output = args[i];
    } else if (arg === "--previous-workspace") {
      i++;
      if (i >= args.length) die("--previous-workspace requires a value");
      previousWorkspace = args[i];
    } else if (arg === "--benchmark") {
      i++;
      if (i >= args.length) die("--benchmark requires a value");
      benchmarkPath = args[i];
    } else if (arg.startsWith("-")) {
      die(
        `Unknown option: ${arg}`,
        "Run with --help for usage information."
      );
    } else {
      if (iterationDir) {
        die(
          `Unexpected argument: ${arg}`,
          "Only one positional argument (iteration-dir) is expected."
        );
      }
      iterationDir = arg;
    }

    i++;
  }

  if (!iterationDir) {
    die(
      "Missing required argument: <iteration-dir>",
      "Usage: bun run eval-viewer/generate-review.ts <iteration-dir> --skill-name <name>\n    Run with --help for more information."
    );
  }

  if (!skillName) {
    die(
      "Missing required option: --skill-name <name>",
      "Usage: bun run eval-viewer/generate-review.ts <iteration-dir> --skill-name <name>\n    Run with --help for more information."
    );
  }

  return {
    iterationDir: resolve(iterationDir),
    skillName,
    output: output ? resolve(output) : null,
    previousWorkspace: previousWorkspace ? resolve(previousWorkspace) : null,
    benchmarkPath: benchmarkPath ? resolve(benchmarkPath) : null,
  };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main(): void {
  const args = parseArgs();

  // Validate iteration directory
  if (!existsSync(args.iterationDir)) {
    die(`Directory not found: ${args.iterationDir}`);
  }

  if (!statSync(args.iterationDir).isDirectory()) {
    die(`Not a directory: ${args.iterationDir}`);
  }

  // Discover evals
  console.error(`  Discovering evals in ${args.iterationDir}…`);
  const evalData = discoverEvals(args.iterationDir);
  console.error(`  Found ${evalData.length} eval case(s)`);

  // Load previous data if provided
  let previousData: EvalData[] | null = null;
  if (args.previousWorkspace) {
    if (existsSync(args.previousWorkspace) && statSync(args.previousWorkspace).isDirectory()) {
      console.error(`  Loading previous iteration from ${args.previousWorkspace}…`);
      try {
        previousData = discoverEvals(args.previousWorkspace);
        console.error(`  Found ${previousData.length} previous eval case(s)`);
      } catch {
        console.error(`  ⚠ Could not load previous workspace, skipping comparison`);
        previousData = null;
      }
    } else {
      console.error(`  ⚠ Previous workspace not found: ${args.previousWorkspace}, skipping`);
    }
  }

  // Load benchmark data
  const benchmarkPath = args.benchmarkPath || join(args.iterationDir, "benchmark.json");
  let benchmarkData: object | null = null;
  if (existsSync(benchmarkPath)) {
    console.error(`  Loading benchmark from ${benchmarkPath}…`);
    benchmarkData = readJsonSafe(benchmarkPath);
  } else {
    console.error(`  No benchmark.json found (looked at ${benchmarkPath})`);
  }

  // Read template
  const templatePath = join(import.meta.dir, "viewer-template.html");
  if (!existsSync(templatePath)) {
    die(
      `Template not found: ${templatePath}`,
      "The viewer-template.html file should be in the same directory as this script."
    );
  }

  let template = readFileSync(templatePath, "utf-8");

  // Replace placeholders
  template = template.replace(
    /\/\*__EVAL_DATA__\*\/\[\]/,
    `/*__EVAL_DATA__*/${JSON.stringify(evalData)}`
  );

  template = template.replace(
    /\/\*__BENCHMARK_DATA__\*\/null/,
    `/*__BENCHMARK_DATA__*/${JSON.stringify(benchmarkData)}`
  );

  template = template.replaceAll(
    "/*__SKILL_NAME__*/",
    args.skillName
  );

  template = template.replace(
    /\/\*__PREVIOUS_DATA__\*\/null/,
    `/*__PREVIOUS_DATA__*/${JSON.stringify(previousData)}`
  );

  // Determine output path
  const outputPath = args.output || join(args.iterationDir, "review.html");

  // Write output
  writeFileSync(outputPath, template, "utf-8");

  const fileSize = statSync(outputPath).size;
  const sizeStr = fileSize < 1024 * 1024
    ? `${(fileSize / 1024).toFixed(1)}KB`
    : `${(fileSize / (1024 * 1024)).toFixed(1)}MB`;

  console.error(`\n  ✓ Review page generated: ${outputPath} (${sizeStr})`);
  console.error(`    open ${outputPath}\n`);
}

main();
