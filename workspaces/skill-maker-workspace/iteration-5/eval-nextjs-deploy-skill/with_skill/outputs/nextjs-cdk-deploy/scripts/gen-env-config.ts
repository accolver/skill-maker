#!/usr/bin/env bun

/**
 * gen-env-config — Generate environment-specific CDK configuration files
 * for Next.js deployments.
 */

const HELP = `
gen-env-config — Generate CDK environment configuration for Next.js deployment.

USAGE
  bun run scripts/gen-env-config.ts --env <name> --region <region>
  bun run scripts/gen-env-config.ts --env staging --region us-east-1 --domain staging.example.com

ARGUMENTS
  --env <name>        Environment name: staging or production (required)
  --region <region>   AWS region (default: us-east-1)
  --domain <domain>   Custom domain name
  --account <id>      AWS account ID (auto-detected if not provided)
  --api-url <url>     API base URL for NEXT_PUBLIC_API_URL
  --output <dir>      Output directory (default: cdk/config)
  --help              Show this help message

OUTPUT
  JSON to stdout with fields:
    file_path         - Path to the generated config file
    config            - The generated configuration object
    env_vars          - Environment variables to set during build

EXIT CODES
  0   Success
  1   Error (invalid arguments)

EXAMPLES
  bun run scripts/gen-env-config.ts --env staging --region us-east-1
  bun run scripts/gen-env-config.ts --env production --region us-east-1 --domain example.com --account 123456789012
`.trim();

import { mkdirSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";

// --- Types ---

interface EnvConfig {
  env: { account: string; region: string };
  domainName: string | null;
  apiUrl: string;
  logLevel: string;
  enableSourceMaps: boolean;
  cacheTtl: number;
  minInstances: number;
  environment: string;
}

interface GenerationResult {
  file_path: string;
  config: EnvConfig;
  env_vars: Record<string, string>;
}

// --- Defaults ---

const DEFAULTS: Record<string, Partial<EnvConfig>> = {
  staging: {
    logLevel: "debug",
    enableSourceMaps: true,
    cacheTtl: 60,
    minInstances: 1,
  },
  production: {
    logLevel: "warn",
    enableSourceMaps: false,
    cacheTtl: 86400,
    minInstances: 3,
  },
};

// --- Main ---

function main(): void {
  const args = process.argv.slice(2);

  if (args.includes("--help") || args.includes("-h")) {
    console.log(HELP);
    process.exit(0);
  }

  const getArg = (flag: string): string | undefined => {
    const idx = args.indexOf(flag);
    return idx !== -1 ? args[idx + 1] : undefined;
  };

  const envName = getArg("--env");
  if (!envName || !["staging", "production"].includes(envName)) {
    console.error("Error: --env must be 'staging' or 'production'");
    process.exit(1);
  }

  const region = getArg("--region") || "us-east-1";
  const domain = getArg("--domain") || null;
  const account = getArg("--account") || "REPLACE_WITH_ACCOUNT_ID";
  const apiUrl = getArg("--api-url") || `https://api-${envName}.example.com`;
  const outputDir = getArg("--output") || "cdk/config";

  const defaults = DEFAULTS[envName] || DEFAULTS.staging;

  const config: EnvConfig = {
    env: { account, region },
    domainName: domain,
    apiUrl,
    logLevel: defaults.logLevel!,
    enableSourceMaps: defaults.enableSourceMaps!,
    cacheTtl: defaults.cacheTtl!,
    minInstances: defaults.minInstances!,
    environment: envName,
  };

  // Generate TypeScript config file
  const tsContent = `// Auto-generated by gen-env-config.ts
// Environment: ${envName}

export const config = ${JSON.stringify(config, null, 2)};
`;

  // Write file
  if (!existsSync(outputDir)) {
    mkdirSync(outputDir, { recursive: true });
  }

  const filePath = join(outputDir, `${envName}.ts`);
  writeFileSync(filePath, tsContent, "utf-8");
  console.error(`Generated: ${filePath}`);

  const envVars: Record<string, string> = {
    NEXT_PUBLIC_API_URL: config.apiUrl,
    NEXT_PUBLIC_ENV: envName,
    NODE_ENV: "production",
  };

  if (config.domainName) {
    envVars.NEXT_PUBLIC_DOMAIN = config.domainName;
  }

  const result: GenerationResult = {
    file_path: filePath,
    config,
    env_vars: envVars,
  };

  console.log(JSON.stringify(result, null, 2));
}

main();
